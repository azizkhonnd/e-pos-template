function changeLanguage(language) {
    document.querySelectorAll(".nav-link").forEach((element) => {
        const key = element.getAttribute("data-key");
        element.textContent = translations[language][key] || key;
    });

    document.querySelectorAll("[data-key]").forEach((element) => {
        const key = element.getAttribute("data-key");
        if (translations[language][key]) {
            element.textContent = translations[language][key];
        }
    });

    const selectedOption = document.querySelector(`.dropdown-menu li[data-value="${language}"]`);
    const iconUrl = selectedOption.getAttribute("data-icon");

    const selectedText = selectedOption.textContent;
    document.getElementById("selected-flag").src = iconUrl;
    document.getElementById("selected-text").textContent = selectedText.trim();

    localStorage.setItem("preferredLanguage", language);

}

function setLanguage(lang) {
    const elements = document.querySelectorAll('[data-key]');

    elements.forEach(el => {
        const key = el.getAttribute('data-key');
        const translation = translations[lang][key];

        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.setAttribute('placeholder', translation);
        } else {
            el.textContent = translation;
        }
    });
}


document.querySelectorAll('.dropdown-menu li').forEach(option => {
    option.addEventListener('click', function () {
        const selectedLanguage = this.getAttribute('data-value');
        changeLanguage(selectedLanguage);
        dropdown.classList.remove('open');
    });
});

document.addEventListener('click', function (event) {
    const isClickInside = dropdown.contains(event.target);
    if (!isClickInside) {
        dropdown.classList.remove('open');
    }
});

document.addEventListener("DOMContentLoaded", function () {
    const savedLanguage = localStorage.getItem("preferredLanguage") || "uz";
    changeLanguage(savedLanguage);
});


